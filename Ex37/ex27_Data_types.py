# Boolean variables
is_sunny = True
is_raining = False

# None variable
temperature = None

# Bytes variable
byte_array = b'Hello, world!'

# String variables
greeting = "Hello"
name = "John"

# Numeric variables
integer_num = 42
float_num = 3.14

# List variable
my_list = [1, 2, 3, 4, 5]

# Dictionary variable
my_dict = {'name': 'John', 'age': 30, 'city': 'New York'}

# Print statements
print(is_sunny)
print(is_raining)
print(temperature)
print(byte_array)
print(greeting + ", " + name)
print(integer_num)
print(float_num)
print(my_list[2])
print(my_dict['name'])

# Modifying list
my_list[0] = 10
print(my_list)

# Modifying dictionary
my_dict['age'] = 40
print(my_dict)

