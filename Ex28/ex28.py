# Exercise 28 : Boolean Practise

"""
Operator	Name	                Example	Try it
==	        Equal	                    x == y
!=	        Not equal	                x != y
>	        Greater than	            x > y
<	        Less than	                x < y
>=	        Greater than or equal to	x >= y
<=	        Less than or equal to	    x <= y
"""

print("is 10 equal to 100: ", 10 == 100)
print("is 10 equal to 10: ", 10 == 10)
print("is 10 not equal to 100: ", 10 != 100)
print("is 10 not equal to 10: ", 10 != 10)
print("is 10 greater to 100: ", 10 > 100)
print("is 100 greater to 10: ", 100 > 10)
print("is 10 less than 100: ", 10 < 100)
print("is 100 less than 10: ", 100 < 10)
print("is 10 greater than or equal to 100: ", 10 >= 100)
print("is 10 greater than or equal to 10: ", 10 >= 10)
print("is 10 less than or equal to 100: ", 10 <= 100)
print("is 10 less than or equal to 10: ", 10 <= 10)

